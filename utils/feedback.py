def get_final_feedback(correct: int, incorrect: int) -> str:
    """Вернуть краткий мотивационный текст по итогам теста."""
    if correct == 0 and incorrect == 0:
        return "Провал — это начало, а не конец. Запустите тест заново и покажите прогресс."

    if correct > 0 and incorrect == 0:
        return "Идеально! Ваш труд принес результат. Продолжайте в том же ключе."

    if incorrect >= 5:
        return "Отдохните, повторите материал и возвращайтесь сильнее."

    if correct >= 4 and incorrect <= 1:
        return "Почти идеал! Вы движетесь вперёд — осталось совсем немного."

    if correct >= incorrect:
        return "Хороший результат! Вы растёте, и это заметно. Двигайтесь дальше."

    return "Это шаг в нужном направлении. Ошибки не мешают — они помогают."
